// This is a line comment
function sum(a, b) {
  return a + b;
}

/* This is a block comment
 */
function sub(a, b) {
  return a - b;
}

/**
 * Sample function
 * @constructor
 * @param {number} a  First multiplier
 * @param {number} b  Second multiplier
 * @return
 */

function mul(a, b) {
  return a * b;
}